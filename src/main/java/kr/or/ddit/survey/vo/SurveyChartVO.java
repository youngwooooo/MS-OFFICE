package kr.or.ddit.survey.vo;


public class SurveyChartVO {
	private int qustnrNo;
	private int qesitmNo;
	private int exNo;
	private String exCn;
	private int rspnsCount;
	
	public int getQustnrNo() {
		return qustnrNo;
	}
	public void setQustnrNo(int qustnrNo) {
		this.qustnrNo = qustnrNo;
	}
	public int getQesitmNo() {
		return qesitmNo;
	}
	public void setQesitmNo(int qesitmNo) {
		this.qesitmNo = qesitmNo;
	}
	public int getExNo() {
		return exNo;
	}
	public void setExNo(int exNo) {
		this.exNo = exNo;
	}
	public String getExCn() {
		return exCn;
	}
	public void setExCn(String exCn) {
		this.exCn = exCn;
	}
	public int getRspnsCount() {
		return rspnsCount;
	}
	public void setRspnsCount(int rspnsCount) {
		this.rspnsCount = rspnsCount;
	}
	
	@Override
	public String toString() {
		return "SurveyChartVO [qustnrNo=" + qustnrNo + ", qesitmNo=" + qesitmNo + ", exNo=" + exNo + ", exCn=" + exCn
				+ ", rspnsCount=" + rspnsCount + "]";
	}
	
	
	
}
